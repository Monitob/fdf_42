#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*str;

	str = (char *)b;
	i = 0;
	while (len > i)
	{
		str[i] = (UCHAR)c;
		i++;
	}
	return (b);
}
